package com.farmer.dto;

public class PortalStatsDTO {
    private Integer publicCycleCount;
    private Integer productCount;
    private Integer farmerCount;
    private Integer orderCount;

    public Integer getPublicCycleCount() { return publicCycleCount; }
    public void setPublicCycleCount(Integer publicCycleCount) { this.publicCycleCount = publicCycleCount; }
    public Integer getProductCount() { return productCount; }
    public void setProductCount(Integer productCount) { this.productCount = productCount; }
    public Integer getFarmerCount() { return farmerCount; }
    public void setFarmerCount(Integer farmerCount) { this.farmerCount = farmerCount; }
    public Integer getOrderCount() { return orderCount; }
    public void setOrderCount(Integer orderCount) { this.orderCount = orderCount; }
}
